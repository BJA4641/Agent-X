-- 1. Archive tables (keep forensics, clear the working tables)
create table if not exists jobs_archive (like jobs including all);
create table if not exists board_items_archive (like board_items including all);

-- 2. Archive + remove the 7,220 failed write_script jobs
insert into jobs_archive
  select * from jobs
  where status = 'failed' and job_type = 'creative.write_script'
  on conflict do nothing;

delete from jobs
  where status = 'failed' and job_type = 'creative.write_script';

-- 3. Archive + remove ALL flood-generated 'idea' board items.
--    (Ideation is cheap; fresh, deduped ideas will be regenerated by a
--     fixed pipeline. Keeping 7,307 junk rows poisons every query.)
insert into board_items_archive
  select * from board_items where status = 'idea'
  on conflict do nothing;

delete from board_items where status = 'idea';

-- 4. Reset any stuck in-progress job back to queued (worker will re-claim
--    after Batch 2 deploy; harmless if the row no longer exists)
update jobs
  set status = 'queued', claimed_by = null
  where status = 'in_progress';

-- 5. Clear any queued write_script jobs pointing at now-archived ideas
delete from jobs
  where status = 'queued' and job_type in ('creative.write_script','editorial.plan_one');

-- 6. Enforce the ONE-ACTIVE-ACCOUNT rule: pause everything.
--    After Batch 2 is deployed and verified, unpause exactly ONE account
--    from the dashboard (Projects -> account -> resume).
update project_accounts set paused = true where paused = false;

-- 7. Verification snapshot — run this block and eyeball the output
select 'jobs_failed_remaining' as metric, count(*)::text as value from jobs where status='failed'
union all select 'jobs_queued', count(*)::text from jobs where status='queued'
union all select 'jobs_in_progress', count(*)::text from jobs where status='in_progress'
union all select 'board_items_idea', count(*)::text from board_items where status='idea'
union all select 'accounts_active', count(*)::text from project_accounts where paused=false
union all select 'archived_jobs', count(*)::text from jobs_archive
union all select 'archived_ideas', count(*)::text from board_items_archive;
